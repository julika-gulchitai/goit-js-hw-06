// Додай слухачів кліків до кнопок, всередині яких збільшуй або зменшуй значення лічильника.
// Оновлюй інтерфейс новим значенням змінної counterValue.

// let counterValue = 0;

let counterValue = document.querySelector('span');
let button = document.querySelectorAll('button');

button[0].addEventListener('click', () => { counterValue.textContent = Number(counterValue.textContent) - 1});
button[1].addEventListener('click', () => { counterValue.textContent = Number(counterValue.textContent) + 1})
    
